//Нахождение суммы всех значений вложенных (двумерных) объеков
//////////////////////////////////////////////////////////////
let obj = {                                     //Создаем вложенный (двумерный) объект с некими элементами
   'key1': {'key1': 1, 'key2': 2, 'key3': 3},   //Создаем ключ 'key1'со значением в виде объекта с некими элементами 
   'key2': {'key1': 4, 'key2': 5, 'key3': 6},   //Создаем ключ 'key2'со значением в виде объекта с некими элементами  
   'key3': {'key1': 7, 'key2': 8, 'key3': 9},   //Создаем ключ 'key3'со значением в виде объекта с некими элементами  
};
let plus = 0;                                   //Создаем переменную plus и присваиваем ей значение 0                 
for (let key in obj) {                          //Запускаем внешний цикл for in и присваеваем в переменную key все ключи объекта obj
   let subObj = obj[key];                       //Создаем переменную subObj и присваеваем ей значения внешних ключей объекта obj
   for (let subKey in subObj) {                 //Запускаем внутненний цикл for in и присваеваем в переменную subKey все ключи вложенных объектов (ключей)
      plus += subObj[subKey];                   //В переменную plus присваеваем значение всех внутренних ключей 
   }
};
console.log(plus)                               //Делаем проверку и получаем результат в виде числа 45   

//Нахождение суммы всех числовых значений вложенных (трехмерных) объеков
////////////////////////////////////////////////////////////////////////
let objOne = {                                             //Создаем вложенный (трехмерный) объект objOne с некими элементами             
   'key1': {'a':1, 'b': 2, 'c': {'d': 3, 'e': 4}, 'f': 5}, //Создаем ключ 'key1' cо значением в виде объекта с некими элементами
   'key2': {'g': 6, 'h': 7},                               //Создаем ключ 'key2' cо значением в виде объекта с некими элементами 
};
let values = 0;                                            //Создаем переменную values и присваеваем ей значение 0 
for (let keyOne in objOne) {                               //Запускаем цикл for in и присваеваем в переменную keyOne все ключи объекта objOne
   let objTwo = objOne[keyOne];                            //Создаем переменную objTwo и присваеваем в неё все значения ключей objOne 
   for (let keyTwo in objTwo) {                            //Запускаем цикл for in и присваеваем в переменную keyTwo все ключи объекта objTwo 
      let objThree = objTwo[keyTwo];                       //Создаем переменную objThree и присваеваем в неё все значения ключей objTwo  
      if (typeof objTwo[keyTwo] == 'number') {             //Если значения ключей objTwo являются числами 
         values += objTwo[keyTwo];                         //Присваемваем в переменную values сумму всех значений ключей objTwo 
      }
      for (let keyThree in objThree) {                     //Запускаем цикл for in и присваеваем в переменную keyThree все ключи объекта objThree    
         values += objThree[keyThree];                     //Присваемваем в переменную values сумму всех значений ключей objThree  
      }
   }
};
console.log(values);                                       //Делаем проверку и получаем результат в виде числа 28



   

