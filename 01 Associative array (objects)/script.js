//Функция проверки элементов объекта на тип данных и их изменение путем умножения
/////////////////////////////////////////////////////////////////////////////////
let menu = {'width': 200,'heigth': 300,'title': 'My menu'}; //Создаем объект menu с некими элементами
function multiplyNumeric() {                                //Создаем функцию умножения числовых значений на 2   
   for (let key in menu) {                                  //Создаем переменную key и присваеваем ей значение всех ключей объекта menu         
      if (typeof menu[key] == 'number') {                   //Если значение объекта является числом
         menu[key] *= 2;                                    //Умножаем данное значение на 2 и изменяем значение в объекте menu
      }
   }
};
multiplyNumeric(menu);                                      //Делаем вызов функции multiplyNumeric
console.log(menu);                                          //Делаем проверку и поулучаем результат в виде значения 400 и 600  

